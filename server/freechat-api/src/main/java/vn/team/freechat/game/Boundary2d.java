package vn.team.freechat.game;

public class Boundary2d {

	public double xMin;
	public double xMax;
	public double zMin;
	public double zMax;
	
	public Boundary2d() {
	}
	
	public Boundary2d(double xMin, double xMax, double zMin, double zMax) {
		super();
		this.xMin = xMin;
		this.xMax = xMax;
		this.zMin = zMin;
		this.zMax = zMax;
	}
	
	
	
}
